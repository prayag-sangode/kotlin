fun main() {
    // Integer (Int) type
    val age: Int = 25
    println("Age: $age")

    // Double (Floating point) type
    val temperature: Double = 36.6
    println("Temperature: $temperature")

    // Boolean type
    val isRaining: Boolean = false
    println("Is it raining? $isRaining")

    // String type
    val greeting: String = "Hello, Kotlin!"
    println(greeting)
}
